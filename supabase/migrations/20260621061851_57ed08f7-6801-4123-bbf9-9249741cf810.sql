matches AS (
  SELECT
    p.id,
    pr.canon,
    -- Use the LAST occurrence so "ถนนกรุงเทพกรีฑา ... จ.ชลบุรี" picks ชลบุรี
    length(p.address) - position(reverse(pr.alias) IN reverse(p.address)) - length(pr.alias) + 2 AS pos
  FROM public.selftest_pii p
  CROSS JOIN provinces pr
  WHERE (p.province IS NULL OR btrim(p.province) = '')
    AND p.address IS NOT NULL
    AND btrim(p.address) <> ''
    AND position(pr.alias IN p.address) > 0
),
best AS (
  SELECT DISTINCT ON (id) id, canon
  FROM matches
  ORDER BY id, pos DESC
)
